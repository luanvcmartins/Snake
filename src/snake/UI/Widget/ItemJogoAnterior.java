    private void setIconImg(int fase) throws Exception{
        ImageIcon imgPreview = new ImageIcon(Base64.getDecoder().decode(buscarImg(fase)));
        imgPreview = new ImageIcon(imgPreview.getImage().getScaledInstance(120, 89, Image.SCALE_DEFAULT));
        
        jLabel1.setIcon(imgPreview);
    }
    
    //aproveitando essa funcao para pegar o nome da fase tbm
    private String buscarImg(int nivel) throws Exception{
        FileReader fr = new FileReader(new File("lvl\\"+nivel+".lvl"));
        BufferedReader br = new BufferedReader(fr);
        
        String s = br.readLine();
        String[] v = s.split(":");
        while(!v[0].equals("img_preview")){
            if(v[0].equals("name"))
                nomeFase = v[1];
            
            s = br.readLine();
            v = s.split(":");
        }
        br.close();
        fr.close();
        
        if(v[0].equals("img_preview"))
            return v[1];
        else
            return null;
    }
